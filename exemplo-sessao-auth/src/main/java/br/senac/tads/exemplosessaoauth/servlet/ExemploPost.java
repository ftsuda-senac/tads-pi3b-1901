package br.senac.tads.exemplosessaoauth.servlet;

import java.io.IOException;
import javax.servlet.ServletException;
import javax.servlet.annotation.WebServlet;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import javax.servlet.http.HttpSession;

/**
 *
 * @author fernando.tsuda
 */
@WebServlet(name = "ExemploPost", urlPatterns = {"/exemplo-post"})
public class ExemploPost extends HttpServlet {

    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        request.getRequestDispatcher("/WEB-INF/jsp/formulario.jsp")
                .forward(request, response);
    }

    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String nome = request.getParameter("nome");
        HttpSession sessao = request.getSession();
        sessao.setAttribute("nome", nome);
//        request.getRequestDispatcher("/WEB-INF/jsp/resultado.jsp")
//                .forward(request, response);
        response.sendRedirect("resultado");
    }

}
